/**
 * Get a random quote from a specific category
 */
export const getRandomQuote = (
  category: keyof typeof MotivationalQuotes,
): string => {
  const quotes = MotivationalQuotes[category];
  const randomIndex = Math.floor(Math.random() * quotes.length);
  return quotes[randomIndex];
};

/**
 * Get a quote with dynamic data (for streak or completion rate)
 */
export const getQuoteWithData = (
  category: keyof typeof MotivationalQuotes,
  data: {streak?: number; rate?: number},
): string => {
  let quote = getRandomQuote(category);

  if (data.streak !== undefined) {
    quote = quote.replace('{streak}', data.streak.toString());
  }

  if (data.rate !== undefined) {
    quote = quote.replace('{rate}', data.rate.toString());
  }

  return quote;
};

/**
 * Get motivational quote based on user's current situation
 */
export const getContextualMotivation = (context: {
  lastOpenedDaysAgo?: number;
  completionRate?: number;
  currentStreak?: number;
  streakBroken?: boolean;
  subgoalsIncomplete?: boolean;
  timeOfDay?: 'morning' | 'evening' | 'weekend';
}): string => {
  // Priority 1: User hasn't opened app
  if (context.lastOpenedDaysAgo && context.lastOpenedDaysAgo >= 1) {
    return getRandomQuote('APP_NOT_OPENED_MOTIVATION');
  }

  // Priority 2: Streak broken
  if (context.streakBroken) {
    return getRandomQuote('STREAK_BROKEN_MOTIVATION');
  }

  // Priority 3: High streak
  if (context.currentStreak && context.currentStreak >= 7) {
    return getQuoteWithData('STREAK_MOTIVATION', {
      streak: context.currentStreak,
    });
  }

  // Priority 4: Time-based motivation
  if (context.timeOfDay === 'morning') {
    return getRandomQuote('MORNING_MOTIVATION');
  }
  if (context.timeOfDay === 'evening') {
    return getRandomQuote('EVENING_MOTIVATION');
  }
  if (context.timeOfDay === 'weekend') {
    return getRandomQuote('WEEKEND_MOTIVATION');
  }

  // Priority 5: Completion rate based
  if (context.completionRate !== undefined) {
    if (context.completionRate >= 80) {
      return getQuoteWithData('HIGH_COMPLETION_MOTIVATION', {
        rate: Math.round(context.completionRate),
      });
    } else if (context.completionRate < 50) {
      return getRandomQuote('LOW_COMPLETION_MOTIVATION');
    }
  }

  // Priority 6: Subgoals incomplete
  if (context.subgoalsIncomplete) {
    return getRandomQuote('USER_NOT_COMPLETING_SUBGOALS_MOTIVATION');
  }

  // Default: All good motivation
  return getRandomQuote('ALL_GOOD_MOTIVATION');
};

